pTempo = ["Ensolarado", "Nublado", "Chuvoso", "Tempestade", "Ensolarado"]

print(pTempo)

print(pTempo[0])  # printando o primeiro elemento da lista

print(pTempo[-1])  # printando o ultimo elemento da lista

print(pTempo[1:3])  # da posição 1 até a 3-1

print(pTempo[:3])  # da posição inicio até a 3-1

print(pTempo[1:])  # da posição 1 à ultimo posição